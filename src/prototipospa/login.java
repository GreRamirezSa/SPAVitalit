    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String usu = usuario.getText();
        String passw = pass.getText();
        int uno = 1;
        int cero = 0;

        Connection miCon;
        miCon = (new ConexionBD()).conectar();
        if (!usu.equals("") && !passw.equals("")) {
            if (miCon != null) {
                try {
                    String sql = "SELECT * FROM USUARIO WHERE USUARIO=" + "'" + usu + "'" + " AND CONTRA=" + "'" + passwo() + "'";
                    Statement stmt = miCon.createStatement();
                    ResultSet r = stmt.executeQuery(sql);

                    if (r.next()) {
                        String cad = r.getString("tipo") + "";
                        if (cad.equals("0")) {
                            pAdmin = new principalAdmin();
                            u.setTipo(cero);
                            pAdmin.setVisible(true);
                            pAdmin.nombre(usu);
                            this.setVisible(false);
                            if (usu.equals(passw)) {
                                actualizarContrasenia();
                            }
                            actualizar2();
                        } else {
                            pAsis = new PrincipalAsistente();
                            u.setTipo(uno);
                            pAsis.setVisible(true);
                            pAsis.nombre(usu);
                            this.setVisible(false);
                            if (usu.equals(passw)) {
                                actualizarContrasenia();
                            }
                            actualizar2();
                        }
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, "Verifique nombre de usuario y contraseña");
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Conexión Fallida" + e.getMessage());
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Verifique nombre de usuario y contraseña");
        }
    }//GEN-LAST:event_jButton1MouseClicked

