    public void actualizar() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            this.table_usuarios.setModel(modelo);

            Connection miCon;
            miCon = (new ConexionBD()).conectar();

            String sql = "select id_producto as \"ID Producto\", nombre as \"Nombre\", precio as \"Precio\", tipo as \"Tipo\", fecha_caducidad as \"Caducidad\", existencias as \"Existencias\" from producto  ";
            Statement stmt = miCon.createStatement();
            ResultSet r = stmt.executeQuery(sql);

            ResultSetMetaData rsMd = r.getMetaData();

            int cantidadColumnas = rsMd.getColumnCount();

            for (int i = 1; i <= cantidadColumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
            }

            while (r.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = r.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
            r.close();
            stmt.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "ConexiÃ³n Fallida" + e.getMessage());
        }
    }

