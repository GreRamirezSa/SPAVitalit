    private void button_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_button_registrarMouseClicked
        String nom = txt_nom.getText();
        String precio = txt_pre.getText();
        String tipo = txt_tipo.getText();
        String exis = txt_exis.getText();
        String fecha = "" + cmbanio.getSelectedItem() + "-" + cmbmes.getSelectedItem() + "-" + cmbdia.getSelectedItem() + "";

        int response = JOptionPane.showConfirmDialog(this, "Nombre: " + nom + "\nPrecio: " + precio + "\nTipo: " + tipo + "\nExistencias: " + exis + "\nFecha: " + fecha + "\nÂ¿Son los datos correctos?", "ATENCIÃN",
                JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        switch (response) {
            case JOptionPane.NO_OPTION:
                break;
            case JOptionPane.YES_OPTION:
                Connection miCon;
                miCon = (new ConexionBD()).conectar();
                if (!nom.equals("") && !precio.equals("") && !tipo.equals("") && !exis.equals("")) {
                    if (miCon != null) {
                        try {

                            String sql = "INSERT INTO PRODUCTO (NOMBRE, PRECIO, TIPO,FECHA_CADUCIDAD,EXISTENCIAS) VALUES ('" + nom + "'," + precio + ",'" + tipo + "','" + fecha + "'," + exis + ")";
                            Statement stmt = miCon.createStatement();
                            stmt.executeUpdate(sql);
                            stmt.close();
                            JOptionPane.showMessageDialog(this, "Insertado exitosamente ");
                        } catch (SQLException e) {
                            JOptionPane.showMessageDialog(this, "Error " + e.getMessage());
                        }
                    }
                    txt_exis.setText("");
                    txt_nom.setText("");
                    txt_pre.setText("");
                    txt_tipo.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Error, no dejar campos vacÃ­os");
                }
                actualizar();
                break;
            case JOptionPane.CLOSED_OPTION:
                break;
            default:
                break;
        }
    }//GEN-LAST:event_button_registrarMouseClicked

