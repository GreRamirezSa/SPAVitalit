    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String fechaE = new SimpleDateFormat("yyyy-MM-dd").format(calendario1.getDate());
        System.out.println(fechaE);
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            this.table_usuarios.setModel(modelo);
            Connection miCon;
            miCon = (new ConexionBD()).conectar();
            System.out.println("query");
            String sql = "select cita.hora,cita.nom_cliente \"nom. cliente\",cita.no_tel as \"no. tel√©fono\", det_cita.servicio from cita inner join det_cita on cita.id_cita=det_cita.id_cita where cita.cabina='" + (1 + cab.getSelectedIndex()) + "' and cita.fecha::text like '" + fechaE + "' order by cita.hora desc";
            System.out.println(sql);
            Statement stmt = miCon.createStatement();
            ResultSet r = stmt.executeQuery(sql);
            ResultSetMetaData rsMd = r.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();

            for (int i = 1; i <= cantidadColumnas; i++) {
                modelo.addColumn(rsMd.getColumnLabel(i));
            }

            while (r.next()) {
                Object[] fila = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    fila[i] = r.getObject(i + 1);
                }
                modelo.addRow(fila);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Conexi√≥n Fallida" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton1MouseClicked

